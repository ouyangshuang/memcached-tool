package com.ouyang.xmemcached.networking;

import com.ouyang.code.yanf4j.core.Session;
import com.ouyang.xmemcached.buffer.BufferAllocator;
import com.ouyang.xmemcached.utils.InetSocketAddressWrapper;

/**
 * Abstract interface for memcached connection.
 *
 * @author dennis
 *
 */
public interface MemcachedSession extends Session {

    boolean isAllowReconnect();

    void setAllowReconnect(boolean allow);

    void setBufferAllocator(BufferAllocator allocator);

    InetSocketAddressWrapper getInetSocketAddressWrapper();

    void destroy();

    @Deprecated
    int getWeight();

    @Deprecated
    int getOrder();

    void quit();

    boolean isAuthFailed();

    void setAuthFailed(boolean authFailed);
}
